// Utilize if/else para escrever um código que retorne o maior de três números. Defina, no começo do seu código, três constantes com os valores que serão comparados.

const a = 50;
const b = 100;
const c = 20;

if (a > b && a > c) {
  console.log("const 'a' é o maior numero");
} else if (b > a && b > c) {
  console.log("const 'b' é o maior numero");
} else if (c > a && c > b) {
  console.log("const 'c' é o maior numero");
}
